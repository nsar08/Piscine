#include <unistd.h>
void ft_putchar(char c)
{
	write (1, &c, 1);
}
void ft_a(void)
{
	char c;
	c = 'z';
	while (c >= 'a')
	{
		ft_putchar(c);
		c--;
	}
}
int main (void)
{
	ft_a();
	return(0);
}
